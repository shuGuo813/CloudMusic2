package com.bw.android.cloudmusic.EventValuesBean;

public class EventMainActivityBean {
    String musicName;
    String musicMakerName;
    boolean flag;

    public boolean isFlag() {
        return flag;
    }

    public void setFlag(boolean flag) {
        this.flag = flag;
    }

    public String getMusicName() {
        return musicName;
    }

    public void setMusicName(String musicName) {
        this.musicName = musicName;
    }

    public String getMusicMakerName() {
        return musicMakerName;
    }

    public void setMusicMakerName(String musicMakerName) {
        this.musicMakerName = musicMakerName;
    }
}
